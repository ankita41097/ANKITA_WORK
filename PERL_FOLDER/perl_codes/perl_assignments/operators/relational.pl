#!usr/bin/perl
use warnings;
use feature 'say';
# FIXME depend on user input

my $a = 15;
my $b = 6;

#Relational Operator

#equal to operator
if ($a == $b){
	print "true\n";}
else{
	print "false\n";}

#not equal to operator
if ($a != $b){
	print "true\n";}
else{
	print "false\n";}

#less than operator
if ($a < $b){
	print "true\n";}
else{
	print "false\n";}

#less than equal operator
if ($a <= $b){
	print "true\n";}
else{
	print "false\n";}

#greater than operator
if ($a > $b){
	print "true\n";}
else{
	print "false\n";}

#greater than equal to operator
if ($a >= $b){
	print "true\n";}
else{
	print "false\n";}
